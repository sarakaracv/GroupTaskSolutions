package Week_6_Methods_Loops;

public class aaaa {
    public static void main(String[] args) {
        int a = 1;
        System.out.println(a);
        a = ++a;
        System.out.println(a);
        a = a++;
        System.out.println(a);
        System.out.println(a);
        a += a;
        System.out.println(a);
        a = a + 1;
        System.out.println(a);
    }
}
